// /pages/api/admin/peptides.js
// Fetch peptide catalog from peptide_tools table
// Range Medical

import { createClient } from '@supabase/supabase-js';

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY
);

export default async function handler(req, res) {
  if (req.method !== 'GET') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { data, error } = await supabase
      .from('peptide_tools')
      .select('id, name, category')
      .order('category')
      .order('name');

    if (error) {
      console.error('Peptides fetch error:', error);
      return res.status(500).json({ error: 'Failed to fetch peptides' });
    }

    return res.status(200).json({ peptides: data || [] });
  } catch (err) {
    console.error('Peptides API error:', err);
    return res.status(500).json({ error: 'Server error' });
  }
}
