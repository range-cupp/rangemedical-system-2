export function getVialsByCategory(categoryId) {
  return VIAL_CATALOG.filter(v => v.category === categoryId && !v.shopHidden && !v.isAddOn);
}

export function getShopVials() {
  return VIAL_CATALOG.filter(v => !v.shopHidden && !v.isAddOn);
}

